/******************************************************************************
* \Syntax          : void Clk_Init(void)                                      
* \Description     : initialize system clock according to user configurations                                   
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*******************************************************************************/
void Clk_Init(void)
{
    RCC.B.BYPASS = 1;
    RCC.B.PWRDN = 1;
    RCC.B.XTAL = CLK_XTAL;
    RCC.B.USESYSDIV = 1;
    RCC.B.SYSDIV = CLK_SYSDIV;
    RCC.B.OSCSRC = 0x0;
    RCC.B.MOSCDIS = 0;
}

